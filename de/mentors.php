        <h2 name="infomentors">Informationen für MentorInnen</h2>
        <span class="smalltitle">Was ist das Discovery Semester?</span>
        <ul>
          <li>Geflüchtete und/oder Asylsuchende Personen (Mentees) können ein Semester lang kostenlos einzelne Lehrveranstaltungen an der ETH Zürich als
              Hörerin oder Hörer besuchen. Als Hörerinnen und Hörer können sie <strong>keine Prüfungen</strong> ablegen.
          </li>
          <li>Mentees erhalten nach Abschluss des Semesters eine Teilnahmebestätigung</li>
          <li>Unterstützung während des Semesters erhalten sie von Mentorinnen und Mentoren, das sind Studierende
              der ETH Zürich aus demselben oder einem ähnlichen Fachgebiet. Ihre Unterstützung umfasst:
              <ul>
                <li>Beratung bei und Auswahl der Vorlesungen sowie Hilfe mit der mystudies-Plattform.</li>
                <li>Hilfe bei der Orientierung auf dem Campus und den ETH-Einrichtungen/Services.</li>
                <li>Integration der Mentees ins Studierendenleben.</li>
              </ul>
          </li>
          <li>Es werden diverse Veranstaltungen für die Teilnehmer angeboten, um den Austausch zwischen Mentees
              und Studierenden zu fördern.
          </li>
        </ul>
        <span class="smalltitle">Aufgaben und Verantwortlichkeiten von MentorInnen:</span>
        <p>MentorInnen helfen den Mentees bei allen Fragen und Anliegen rund um das Studium an der ETH. Zentrale Aufgaben sind:</p>
        <ul>
          <li>Teilnahme am Training zu Beginn des Semesters welches in Zusammenarbeit mit dem Verband der Schweizer Studierendenschaften (VSS)
              organisiert wird.
          </li>
          <li>Beratung bei der Auswahl der Vorlesungen sowie Hilfe mit der mystudies-Plattform.</li>
          <li>Hilfe beim Zurechtfinden auf dem Campus und den ETH Einrichtungen/Services.</li>
          <li>Integration der Mentees ins Studentenleben (z.B. gemeinsames Essen, Sport oder Freunden vorstellen)</li>
          <li>Teilnahme an Social Events für die Teilnehmer des Programms bei denen man andere Mentoren und Mentees kennenlernen
              und sich mit diesen austauschen und mit ihnen Kontakte knüpfen kann.
          </li>
          <li>Feedback und Evaluation des Programms während des Semesters sowie zum Abschluss.</li>
          <li>Kommunikation mit dem Organisationsteam bezüglich des Befindens und der Fortschritte der/des Geflüchteten im Projekt.</li>
        </ul>
        <span class="smalltitle">Voraussetzungen für das Mentorenprogramm:</span>
        <ul>
          <li>Verfügbarkeit in der ersten Woche des Semester (idealerweise schon vorher), um den Mentees bei der Kursauswahl zu helfen.</li>
          <li>Eine bis zwei Stunden Zeit und Motivation die Woche, um den Mentees bei Fragen und Anliegen zu unterstützen.</li>
          <li>Proaktive Kommunikation mit dem Mentee.</li>
          <li>Offenheit gegenüber anderen Kulturen und Freude am Austausch.</li>
        </ul>
