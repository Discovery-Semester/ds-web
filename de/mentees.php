        <h2 name="infomentees">Informationen für geflüchtete Personen/Mentees</h2>
        <span class="smalltitle">Was ist das Discovery Semester?</span>
        <ul>
          <li>Geflüchtete und/oder Asylsuchende Personen (Mentees) können ein Semester lang kostenlos einzelne Lehrveranstaltungen
              an der ETH Zürich als Hörerin oder Hörer besuchen. Als Hörerinnen und Hörer können sie <strong>keine Prüfungen</strong> ablegen.
          </li>
          <li>Mentees erhalten nach Abschluss des Semesters eine Teilnahmebestätigung</li>
          <li>Unterstützung während des Semesters erhalten sie von Mentorinnen und Mentoren, das sind Studierende
              der ETH Zürich aus demselben oder einem ähnlichen Fachgebiet. Ihre Unterstützung umfasst:
              <ul>
                <li>Beratung bei und Auswahl der Vorlesungen sowie Hilfe mit der mystudies-Plattform.</li>
                <li>Hilfe bei der Orientierung auf dem Campus und den ETH-Einrichtungen/Services.</li>
                <li>Integration der Mentees ins Studierendenleben.</li>
              </ul>
          </li>
          <li>Es werden diverse Veranstaltungen für die Teilnehmer angeboten, um den Austausch zwischen Mentees
              und Studierenden zu fördern.
          </li>
          <li>Für eine Anmeldung zu einem Studiengang gelten für Mentees auch nach Teilnahme am Discovery Semester
              die <a href="https://www.ethz.ch/de/studium/anmeldung-bewerbung/bachelor/andere-qual.html">allgemeinen Zulassungsbedingungen</a> der ETH Zürich.
          </li>
        </ul>
        <span class="smalltitle">Voraussetzungen für die Teilnahme am Discovery Semester:</span>
        <ul>
          <li>Akademischer Hintergrund: allgemeine Hochschulreife (z.B. Matura, Abitur) oder bereits angefangenes
              bzw. abgeschlossenes Studium.
          </li>
          <li>Interesse an einem Studiengang, welcher an der ETH angeboten wird (hauptsächlich naturwissenschaftliche
              und technische Studiengänge). Für Interssierte an anderen Studiengängen verweisen wir gerne an das
              <a href="https://www.int.uzh.ch/de/in/refugees/discovery-program.html">Schnupperprogramm</a> der Universität Zürich.
          </li>
          <li>Ausreichende Sprachkenntnisse in Deutsch und/oder Englisch (mind. <a href="http://www.europaeischer-referenzrahmen.de/">Niveau B1</a>)</li>
          <li>Aufenthaltsbewilligung <a href="https://www.sem.admin.ch/sem/de/home/themen/aufenthalt/biometr_auslaenderausweis.html">B</a>,
            <a href="https://www.sem.admin.ch/sem/de/home/themen/aufenthalt/nicht_eu_efta.html">F oder N</a>.</li>
        </ul>
        <span class="smalltitle">Anmeldung und Ablauf des Discovery Semester:</span>
        <ul>
          <li>Asylsuchende und Geflüchtete, die sich für das Hörerprogramm interessieren, können sich ab sofort
              <a href="https://forms.gle/iRryybmrG1mvVrb16">online anmelden</a>. <strong>Bewerbungsschluss ist der 14. Juli 2019.</strong>
          </li>
          <li>Ablauf der Bewerbung: Onlineformular ausfüllen (Fragebogen, kurzes Motivationsschreiben, Aufenthaltsbewilligung,
              gegebenenfalls Abschlusszeugnisse) und falls die Kriterien erfüllt werden, gibt es ein persönliches Gespräch im Juni/Juli.
          </li>
          <li>Aufnahmebestätigung: Anfang August 2019.</li>
          <li>Start: Vorlesungsbeginn des Herbstsemesters (16.09.2019).</li>
          <li>Auswahl der Kurse erfolgt idealerweise schon vor Semesterbeginn, die Deadline ist das Ende der zweiten Semesterwoche</li>
          <li>Dauer: Ein Semester (13 Wochen).</li>
          <li>Ende: Vorlesungsende Herbstsemester (20.12.2019)
        </ul>
        <em>Bei Fragen <a href="mailto:contact@discovery-semester.ch" class="infolink">melde dich</a> gerne bei uns!</em>
