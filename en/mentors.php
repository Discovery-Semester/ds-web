        <h2 name="infomentors">Information for mentors</h2>
        <span class="smalltitle">What is the Discovery Semester?</span>
        <ul>
          <li>Refugees and/or asylum seekers (mentees) with an academic background can visit lectures at ETH free of charge during one
              semester. This does <strong>not</strong> entitle them to take exams.
          </li>
          <li>Mentees receive a confirmation of participation afetr completion of the semester</li>
          <li>Each mentee receives support from their mentor during the semester. The mentors are ETH students from the
              same or a similar major that act as a guide for the mentee. The support encompasses:
              <ul>
                <li>Help with the mystudies platform and the selection of as well as enrolment for courses.</li>
                <li>Help with thr orientation around the campus and the ETH facilities/services.</li>
                <li>Integration into the student life.</li>
              </ul>
          </li>
          <li>During the semester, several events are offered to project participants in order for them to get to know each other and
              foster the exchange between mentees and students.
          </li>
        </ul>
        <span class="smalltitle">Tasks and responsibilities of the mentor:</span>
        <p>Mentors help mentees with all questions and concerns connected to studying at ETH. The key tasks of a mentor include:</p>
        <ul>
          <li>Participation in the “mentor training” at the beginning of the semester organized in cooperation with the Association
              of the Swiss Student Unions (VSS).
          </li>
          <li>Advice on the selection of lectures and assistance with the mystudies platform.</li>
          <li>Helping mentees in finding their way around the campus and the ETH facilities/services.</li>
          <li>Integration of mentees into the student life (for example having lunch together, doing sports or meeting friends)</li>
          <li>Optional attendance of social events during the semester where all project participants are given the chance to get to
              know each other to foster exchange between mentees and students.
          </li>
          <li>Feedback and evaluation of the program during the semester as well as at the end.</li>
          <li>Feedback on the well-being and the progress of the mentee in the project.</li>
        </ul>
        <span class="smalltitle">Requirements for the mentoring program:</span>
        <ul>
          <li>Availability during the first week of the semester (ideally even the week before) to help the mentee choose her/his courses.</li>
          <li>About one to two hours of time and motivation per week to support the mentee with any questions or problems he/she encounters.</li>
          <li>Proactive communication with the mentee.</li>
          <li>You are open towards other cultures and enjoy interacting with different people.</li>
        </ul>
