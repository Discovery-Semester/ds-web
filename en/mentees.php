        <h2 name="infomentees">Information for refugees/mentees</h2>
        <span class="smalltitle">What is the Discovery Semester?</span>
        <ul>
          <li>Refugees and/or asylum seekers (mentees) with an academic background can visit lectures at ETH free of charge during one
              semester. This does <strong>not</strong> entitle them to take exams.
          </li>
          <li>Mentees receive a confirmation of participation after completion of the semester</li>
          <li>Each mentee receives support from their mentor during the semester. The mentors are ETH students from the
              same or a similar major that act as a guide for the mentee. The support encompasses:
              <ul>
                <li>Help with the mystudies platform and the selection of as well as enrolment for courses.</li>
                <li>Help with thr orientation around the campus and the ETH facilities/services.</li>
                <li>Integration into the student life.</li>
              </ul>
          </li>
          <li>During the semester, several events are offered to project participants in order for them to get to know each other and
              foster the exchange between mentees and students.
          </li>
          <li>To enroll as a full time student, the general <a href="https://www.ethz.ch/en/studies/registration-application/bachelor/other-certificates.html">admission requirements</a>
              of ETH Zurich apply.</li>
        </ul>
        <span class="smalltitle">Requirements for applicants:</span>
        <ul>
          <li>Academic background: higher education entrance qualification (eg. Matura, Abitur) or you have started
              your studies or have already completed a degree at another university.
          </li>
          <li>You are interested in a major that is offered at ETH (mostly natural sciences and engineering). If you are
              looking to major in another field, we gladly refer you to the <a href="https://www.int.uzh.ch/en/in/refugees/discovery-program.html">discovery program</a>
              at the University of Zurich.
          </li>
          <li>Sufficient knowledge of German and / or English (at least <a href="https://www.coe.int/en/web/common-european-framework-reference-languages/level-descriptions">level B1</a>)</li>
          <li>Residence permit <a href="https://www.sem.admin.ch/sem/en/home/themen/aufenthalt/biometr_auslaenderausweis.html">B</a>,
            <a href="https://www.sem.admin.ch/sem/en/home/themen/aufenthalt/nicht_eu_efta.html">F or N</a>.
          </li>
        </ul>
        <span class="smalltitle">Registration and course of the Discovery Semester:</span>
        <ul>
          <li>Asylum seekers and refugees who are interested in the program can now <a href="https://forms.gle/Jo5zdFVZdsGLCoYE6">register online</a>.
              <strong>The application deadline is the 14th of July 2019.</strong>
          </li>
          <li>Application procedure: fill out the online form (questionnaire, short letter of motivation, scans of residence permit and diploma
              if available) and if you meet the criteria, you will be a invited to a personal interview in June/July.
          </li>
          <li>Confirmation of admission to the program: beginning of August 2019</li>
          <li>Starts with the fall semester (16.09.2019).</li>
          <li>The courses are ideally chosen before the semester starts, the deadline is the end of the second week of the semester.</li>
          <li>Duration: one semester (13 weeks).</li>
          <li>Ends with the fall semester (20.12.2019).</li>
        </ul>
        <em>If you have any questions feel free to <a href="mailto:contact@discovery-semester.ch" class="infolink">contact us</a>!</em>
